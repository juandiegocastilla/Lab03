    private ImageIcon[] imagenes;

    public void cargarImagenes() throws IOException {
        String ruta = "src/main/resources/"; // Usa la ruta relativa desde el directorio raíz del classpath
        imagenes = new ImageIcon[8];

        for (int i = 0; i < imagenes.length; i++) {
            BufferedImage imagen = ImageIO.read(getClass().getResource(ruta + "man" + (i) + ".png"));
            imagenes[i] = new ImageIcon(imagen);
        }

        // Establecer una imagen para ver si todo funciona
        if (0 < imagenes.length) {
            jLabel1.setIcon(imagenes[0]); // Muestra la primera imagen como ejemplo
        }
    }

